#include "libft.h"

int		ft_strwords(char *str, char c)
{
	int		i;
	int		words;

	i = 0;
	words = 0;
	while (str[i] != '\0')
	{
		if ((str[i] != c && str[i - 1] == c) || (str[i] != c && i == 0))
			words++;
		i++;
	}
	return (words);
}
